        <h2>Growing Across Subjects and Grades</h2>
        <p>AR technology is most commonly used for science-based subjects, such as biology and anatomy, as well as geography and art. However, it is increasingly being used in a wider range of activities and assignments.</p>
        <p>Here's a quick list of potential AR applications for classes ranging from Pre-K to 12th grade.</p>
        <h3 style="padding-bottom: 0px;">Pre-K to 3rd Grade</h3>
        <p><em>Teacher-Guided Content</em></p>
        <ul>
            <li><strong>Homework help</strong> &mdash; Selected images can trigger video tutorials with helpful tips.</li>
            <li><strong>Educational gameplay</strong> &mdash; Timed math challenges can make lessons more engaging.</li>
            <li><strong>Improving literacy</strong> &mdash; Teachers can reinforce reading and spelling skills with flash cards that come to life.</li>
        </ul>
        <h3 style="padding-bottom: 0px;">4th Grade to 12th Grade</h3>
        <p><em>Student-Created Content</em></p>
        <ul>
            <li><strong>Book reports</strong> &mdash; Students can animate synopses and bios with audio and video.</li>
            <li><strong>Classroom safety</strong> &mdash; Triggers placed around the room can demonstrate various aspects of lab safety.</li>
            <li><strong>Open house visits</strong> &mdash; Guests can explore interactive displays and tours.</li>
        </ul>
        <h2>Augmented Reality: Just Another Passing Fad?</h2>
        <p><img src="http://www.aopcatholicschools.org/wp-content/uploads/2017/09/fidget-spinner.gif" class="alignright" width="300">Trendy as AR might be, it's unfair to write it off as the latest tech craze. It has already fundamentally changed the way many people interact with media, games, and even cars.</p>
        <p>Incorporating AR into the classroom builds familiarity with technology that will continually impact entire industries and job markets. More importantly, AR can push our schools forward, enabling students to learn, create, and share concepts in meaningful ways.</p>
