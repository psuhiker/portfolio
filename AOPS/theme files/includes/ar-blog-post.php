        <h2>Seeing Beyond Real Life</h2>
        <p>It wasn't long ago that augmented reality seemed like the stuff of sci-fi movies. But thanks to devices and apps like Google Glass, Snapchat filters, and Pok√©mon Go, AR has already begun to impact our day-to-day lives.</p>
        <p>As this technology has grown in power and popularity, educators and developers have created exciting ways to use AR in the classroom. These tools allow students to actively engage with lesson concepts, visualize shapes in 3D, and even create their own interactive projects, opening an entire new world of educational opportunities.</p>
        <p>And as this emerging trend becomes more common in classroom settings, many experts predict that AR will revolutionize the way students learn in K-12 schools and higher education.</p>
    </div>
</div>

<div class="grid_12 b-bg appear w-text">
    <div class="padding padded">
        <h3 class="text-center">What is Augmented Reality?</h3>
        <p>AR is a live view of the real world overlaid with computer-generated sensory input such as sound, video, or graphics. In other words, viewers see real life topped with a digital overlay. If you've ever watched a televised football game with a digitally inserted first down line, you've seen AR in action.</p>
        <h3 class="text-center">What's The Difference Between AR and VR?</h3>
        <p>Augmented reality and virtual reality both alter our perceptions of our surroundings. The difference is the degree to which our environments are stimulated.</p>
